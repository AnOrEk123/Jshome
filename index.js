let number = prompt("Введите число?");
switch (number) {
  case '0':
      alert( 'ноль' )
      break;
  case '1':
    alert( 'Один' );
    break;
  case '2':
    alert( 'Два' );
    break;
  case '3':
    alert( 'Три' );
    break;
  case '4':
    alert( 'Четыре' );
    break;
    case '5':
        alert( 'Пять' );
  default:
    alert( 'Неизвестное значение' );
}